public class Task{

	private String name;
	private String startDay;
	
	public String getDate() {
		return startDay;
	}

	public void setDate(String startDay) {
		this.startDay = startDay;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
